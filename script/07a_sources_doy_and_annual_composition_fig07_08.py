def load_streamflow_partition(ssp, wastage_frac):
    path = os.path.join(fig_dir_flow, f"FlowComponents_ensemble_{ssp}.csv")
    df = pd.read_csv(path).rename(columns=lambda x: x.strip())
    years = extract_years(df).astype(int)
    waste_frac = wastage_frac.reindex(years).to_numpy()

    # Pull mean discharge (m3/s)
    snowoff = df["Snow Off Glacier_m3yr_mean"]
    sice    = df["Snow + Ice On Glacier_m3yr_mean"]
    rainoff = df["Rain Off Glacier_m3yr_mean"]

    gw_snow = df["GW from Snow_m3yr_mean"]
    gw_rain = df["GW from Rain_m3yr_mean"]
    gw_glac = df["GW from Glacier_m3yr_mean"]

    waste_frac = wastage_frac.reindex(years).values
    waste_sice = sice * waste_frac
    seas_sice  = sice * (1 - waste_frac)
    waste_gw   = gw_glac * waste_frac
    seas_gw    = gw_glac * (1 - waste_frac)

    # Categories (still m3/s)
    Snow_q = snowoff + gw_snow  + seas_gw+ seas_sice
    Rain_q = rainoff + gw_rain
    Wast_q = waste_sice + waste_gw

    # Convert to m3/s
    SEC_PER_YR = 365.0 * 24 * 3600 
    Snow = Snow_q / SEC_PER_YR
    Rain = Rain_q / SEC_PER_YR
    Wastage = Wast_q / SEC_PER_YR
    Total_calc = Snow + Rain + Wastage

    # If your ensemble file has total volume:
    if "Total_m3yr_mean" in df.columns:
        Total_file = df["Total_m3yr_mean"].to_numpy()
        rel_err = (Total_calc.to_numpy() - Total_file) / Total_file
        print(ssp, "streamflow closure median rel err:", np.nanmedian(np.abs(rel_err)))

    return pd.DataFrame({"Year": years, "Snow": Snow, "Rain": Rain, "Wastage": Wastage})


