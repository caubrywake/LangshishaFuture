source_stats_rows = []

def compute_source_period_stats(df_sources, year_min, year_max, ssp, period_label):
    """
    df_sources: daily Snow/Rain/Wastage/Total, index=datetime
                (from daily_sources_from_components)
    Computes mean discharge and fraction of total for each source,
    plus the DOY and date of peak contribution, over the given year range.
    """
    df = df_sources.copy()
    df["Year"] = df.index.year
    df["DOY"]  = df.index.dayofyear

    # restrict to requested years and drop Feb 29
    mask = (
        (df["Year"] >= year_min) &
        (df["Year"] <= year_max) &
        (df["DOY"] <= 365)
    )
    df = df.loc[mask]

    sources_to_plot = ["Snow", "Rain", "Wastage"]

    # mean discharge over the period (m³/s)
    mean_total = df["Total"].mean()

    for src in sources_to_plot:
        mean_src = df[src].mean()
        frac_pct = 100.0 * mean_src / mean_total if mean_total > 0 else np.nan

        # peak day and date within the period
        idx_peak  = df[src].idxmax()
        doy_peak  = int(df.loc[idx_peak, "DOY"])
        date_peak = idx_peak.date().isoformat()

        source_stats_rows.append({
            "SSP": ssp,
            "Period": period_label,          # "Early" or "Late"
            "Year_min": year_min,
            "Year_max": year_max,
            "Source": src,
            "Mean_discharge_m3s": mean_src,
            "Mean_total_m3s": mean_total,
            "Fraction_of_total_%": frac_pct,
            "Peak_DOY": doy_peak,
            "Peak_date": date_peak,
        })


# --- loop over SSPs and periods using source-based daily series ---
for ssp in ssps:
    print(f"\nComputing source-based stats for {ssp}...")

    # 1) annual wastage fraction (same as in ternary script)
    wfrac_annual = load_wastage_fraction(ssp)

    # 2) ensemble-mean daily SPHY components
    df_daily = load_ensemble_daily_components(ssp)

    # 3) convert to daily Snow / Rain / Wastage / Total
    df_sources = daily_sources_from_components(df_daily, wfrac_annual)

    # 4) stats for early and late periods
    compute_source_period_stats(df_sources, early_range[0], early_range[1], ssp, "Early")
    compute_source_period_stats(df_sources, late_range[0],  late_range[1],  ssp, "Late")


# --- build table and save to CSV ---
source_stats_df = pd.DataFrame(source_stats_rows)

out_source_stats_csv = os.path.join(out_dir, "SourceHydrographs_DOY_stats.csv")
source_stats_df.to_csv(out_source_stats_csv, index=False)
print(f"\nSaved source-based stats to:\n  {out_source_stats_csv}")


