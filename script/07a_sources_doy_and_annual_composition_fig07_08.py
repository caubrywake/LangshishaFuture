# ---- helper: load ensemble-mean daily components for one SSP ----
def load_ensemble_daily_components(ssp):
    """
    For a given SSP, load all GCM daily TSS files for each flow component,
    align them on a common datetime index, and return df_mean:
      index = datetime, columns = FLOW_FILES keys (m3/s, ensemble mean).
    """
    gcm_dirs = [
        d for d in os.listdir(base_dir)
        if os.path.isdir(os.path.join(base_dir, d))
    ]
    if not gcm_dirs:
        raise RuntimeError(f"No GCM directories found in {base_dir}")

    comp_series = {comp: [] for comp in FLOW_FILES.keys()}

    for gcm in gcm_dirs:
        ssp_dir = os.path.join(base_dir, gcm, ssp)
        if not os.path.isdir(ssp_dir):
            continue

        for comp, fname in FLOW_FILES.items():
            tss_path = os.path.join(ssp_dir, fname)
            if not os.path.exists(tss_path):
                print(f"⚠️ Missing {tss_path}, skipping ({gcm}, {ssp}, {comp})")
                continue

            s = read_tss(tss_path, start_date, colname=comp)
            s.name = f"{gcm}_{comp}"
            comp_series[comp].append(s)

    df_mean = pd.DataFrame()
    for comp, series_list in comp_series.items():
        if not series_list:
            continue
        df_all = pd.concat(series_list, axis=1)
        df_mean[comp] = df_all.mean(axis=1)

    df_mean = df_mean.sort_index()
    return df_mean

# ---- helper: daily Snow/Rain/Wastage from daily components + annual f_waste ----
def daily_sources_from_components(df_daily, wastage_frac):
    """
    df_daily: ensemble-mean daily components (FLOW_FILES keys)
    wastage_frac: Series indexed by year with f_waste = wastage / total glacier melt

    Returns DataFrame with daily Snow, Rain, Wastage, Total (m3/s).
    """
    df = df_daily.copy()
    df["Year"] = df.index.year
    df["DOY"]  = df.index.dayofyear

    # map annual wastage fraction to each day
    df["f_waste"] = df["Year"].map(wastage_frac)
    df["f_waste"] = df["f_waste"].fillna(0.0)  # pre-2017 etc.

    f = df["f_waste"].values

    snowoff = df["Snow Off Glacier"]
    sice    = df["Snow + Ice On Glacier"]
    rainoff = df["Rain Off Glacier"]

    gw_snow = df["GW from Snow"]
    gw_rain = df["GW from Rain"]
    gw_glac = df["GW from Glacier"]

    # split glacier components into seasonal vs wastage (constant fraction per year)
    waste_sice = sice * f
    seas_sice  = sice * (1.0 - f)

    waste_gw   = gw_glac * f
    seas_gw    = gw_glac * (1.0 - f)

    Snow     = snowoff + gw_snow + seas_sice + seas_gw
    Rain     = rainoff + gw_rain
    Wastage  = waste_sice + waste_gw
    Total    = Snow + Rain + Wastage  # should closely match df["Total"]

    out = pd.DataFrame({
        "Snow": Snow,
        "Rain": Rain,
        "Wastage": Wastage,
        "Total": Total
    }, index=df.index)

    # keep year & DOY for climatologies
    out["Year"] = df["Year"]
    out["DOY"]  = df["DOY"]
    return out

# ---- helper: DOY climatology for a given period ----
def doy_climatology_sources(df_sources, year_min, year_max):
    df = df_sources.copy()
    mask = (df["Year"] >= year_min) & (df["Year"] <= year_max) & (df["DOY"] <= 365)
    df = df.loc[mask]

    grp = df.groupby("DOY")[["Snow", "Rain", "Wastage", "Total"]].mean()
    grp = grp.reindex(range(1, 366))
    return grp


