def load_wastage_fraction(ssp):
    """Load annual wastage fraction from ensemble_balance_SSP.csv"""
    path = os.path.join(fig_dir_balance, f"ensemble_balance_{ssp}.csv")
    df = pd.read_csv(path)
    df["year"] = df["year"] if "year" in df.columns else df["year_mean"]
    df = df.set_index("year")
    return df["imbalanced_mm_mean"] / df["total_mm_mean"]

def load_precip_inputs_with_wastage(ssp):
    """
    Panel (a): Input composition in VOLUME units (m3/yr):
      - Snow and Rain: convert basin-mean mm/yr to m3/yr using A_BASIN_M2
      - Wastage: use imbalanced_m3_mean directly from ensemble_balance (already m3/yr)
    """

    # --- Load precip (Snow + Rain, in mm over basin) ---
    path_p = os.path.join(fig_dir_precip, f"Precip_flux_{ssp}_ENSEMBLE.csv")
    dp = pd.read_csv(path_p).rename(columns=lambda x: x.strip())

    dp["Year"] = 2017 + (dp["YearIndex"] - 1)
    dp = dp[["Year", "Snow_mm_mean", "Rain_mm_mean"]]

    # Convert mm -> m3 using basin area
    dp["Snow"] = (dp["Snow_mm_mean"] / 1000.0) * A_BASIN_M2
    dp["Rain"] = (dp["Rain_mm_mean"] / 1000.0) * A_BASIN_M2
    dp = dp[["Year", "Snow", "Rain"]]

    # --- Load wastage as m3/yr from glacier balance ---
    path_w = os.path.join(fig_dir_balance, f"ensemble_balance_{ssp}.csv")
    dw = pd.read_csv(path_w)

    if "year" in dw.columns:
        dw["Year"] = dw["year"]
    elif "year_mean" in dw.columns:
        dw["Year"] = dw["year_mean"]
    else:
        raise ValueError("Cannot find year column in wastage file.")

    # Use *volume* wastage
    if "imbalanced_m3_mean" not in dw.columns:
        raise ValueError("Expected column 'imbalanced_m3_mean' not found in ensemble_balance file.")

    dw = dw[["Year", "imbalanced_m3_mean"]].rename(columns={"imbalanced_m3_mean": "Wastage"})

    # Merge
    df = pd.merge(dp, dw, on="Year", how="left")

    return df  # Year, Snow(m3/yr), Rain(m3/yr), Wastage(m3/yr)


def extract_years(df):
    """
    Try to get a proper Year series from the dataframe.
    Handles:
      - 'Year' column (int)
      - 'datetime' as string dates
      - 'datetime' as plain year numbers (e.g., 2025, 2026)
    """
    if "Year" in df.columns:
        return df["Year"].astype(int)

    if "datetime" in df.columns:
        col = df["datetime"]

        # If it's numeric, assume it's already the year
        if np.issubdtype(col.dtype, np.number):
            return col.astype(int)

        # Otherwise, parse as datetime string
        return pd.to_datetime(col).dt.year

    raise ValueError("No 'Year' or 'datetime' column found to extract years.")


