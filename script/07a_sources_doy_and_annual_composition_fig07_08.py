def read_tss(file_path: str, start_date: pd.Timestamp, colname="col_1") -> pd.Series:
    """Read SPHY .tss file and return the first data column as a Series indexed by datetime."""
    with open(file_path, "r") as f:
        lines = f.readlines()

    start_idx = None
    for i, line in enumerate(lines):
        parts = line.strip().split()
        if len(parts) >= 2 and parts[0].lstrip("-").isdigit():
            start_idx = i
            break
    if start_idx is None:
        raise ValueError(f"No numeric data found in {file_path}")

    df = pd.read_csv(file_path,sep=r"\s+",skiprows=start_idx, header=None, engine="python")
    df.columns = ["day"] + [f"col_{i}" for i in range(1, df.shape[1])]

    t0 = df["day"].iloc[0]
    dt = start_date + pd.to_timedelta(df["day"] - t0, unit="D")
    s = pd.Series(df["col_1"].astype(float).values, index=dt, name=colname).sort_index()
    return s



